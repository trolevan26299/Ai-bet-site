const updateHomeValue = (data: any) => {
  const newData = data.map((item: any) => {
    const newSpreads = item.bets.spreads.map((spread: any) => {
      if (spread.home !== undefined && spread.away !== undefined) {
        spread.home = Math.round((Math.random() * 2000 - 1000) / 10) / 100;
        spread.away = Math.round((Math.random() * 2000 - 1000) / 10) / 100;
      }
      return spread;
    });
    const newTotals = item.bets.totals.map((spread: any) => {
      if (spread.over !== undefined && spread.under !== undefined) {
        spread.over = Math.round((Math.random() * 2000 - 1000) / 10) / 100;
        spread.under = Math.round((Math.random() * 2000 - 1000) / 10) / 100;
      }
      return spread;
    });
    return { ...item, bets: { ...item.bets, spreads: newSpreads, totals: newTotals } };
  });
  return newData;
};

